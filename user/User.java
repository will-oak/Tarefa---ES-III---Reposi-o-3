public class User {
    private String login;
    private String password;
    private String token;

    private String usuario;

    public String getUsuario() {
        return usuario;
    }

    public String getLogin() {
        return login;
    }

    public String getPassword() {
        return password;
    }

    public String getToken() {
        return token;
    }
}
